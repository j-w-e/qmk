bool is_oneshot_cancel_key(uint16_t keycode) {
    switch (keycode) {
        case OS_SYM:
        case LA_NAV:
        case LA_MOUSE:
        case LA_NUM:
            return true;
        default:
            return false;
    }
}

bool is_oneshot_ignored_key(uint16_t keycode) {
    switch (keycode) {
        case OS_SYM:
        case LA_NAV:
        case LA_MOUSE:
        case LA_NUM:
        /* case JWE_SHFT: */
        case OS_SHFT:
        case OS_CTRL:
        case OS_ALT:
        case OS_CMD:
            return true;
        default:
            return false;
    }
}

bool is_oneshot_layer_cancel_key(uint16_t keycode) {
    switch (keycode) {
        case LA_NAV:
            if(layer_state_is(SYM)) {
                return false;
            }
        case OS_SYM:
        case LA_MOUSE:
        case LA_NUM:
            return true;
        default:
            return false;
    }
}

